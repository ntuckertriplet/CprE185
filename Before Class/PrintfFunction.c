#include <stdio.h>


int main() {

  // \n is a new line function
  printf("Hello\nWorld");

  //how to actually print out a quotation mark in c
  printf("Hello\"World");

  //how to print a number with the % modifier
  printf("My favorite number is %d", 25);

  //printing out a double
  printf("My favorite number is %f", 25.5);

  //using % to include text
  printf("My favorite %s is %d", "number", 25);

  return 0;
}
